
public class Task1234PrintNumbers {

	public static void main(String[] args) {
		
		for (int x = 1; x <= 100; x++){
			System.out.print(x);
		}
		for (int x = -20; x <= 50; x++){
			System.out.print(x);
		}
		for (int x = -9; x<=10; x+=2){
			System.out.print(x);
		}
		for (int x = 10; x >=1; x--){
			System.out.print(x);
		}

	}

}
