
public class Task2Operations {

	public static void main(String[] args) {

		float a = 9.35F;
		int b = 5;
		float c = a + b;
		System.out.println("a+b = " + c);
		float d = a - b;
		System.out.println("a-b = " + d);
		float e = a * b;
		System.out.println("a*b = " + e);
		float f = a / b;
		System.out.println("a/b = " + f);
		float g = a % b;
		System.out.println("a%b = " + g);

	}

}
